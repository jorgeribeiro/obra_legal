package br.gov.ma.tce.obralegal.server.statuspendencia;

public enum StatusPendencia {

	/*
	 * Pendente: enviada pela fiscalização
	 * Resolvido: retornada pelo jurisdicionado e aceita pela fiscalização
	 * Retornada: retornada pelo jurisdicionado e ainda não aceita pela fiscalização
	 */
	PENDENTE("PENDENTE"), RESOLVIDO("RESOLVIDO"), RETORNADA("RETORNADA");

	private String descricao;

	private StatusPendencia(String descricao) {
		this.descricao = descricao;
	}
	
	public String getDescricao() {
		return descricao;
	}

}
