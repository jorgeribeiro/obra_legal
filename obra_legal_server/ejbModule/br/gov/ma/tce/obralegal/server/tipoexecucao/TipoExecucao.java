package br.gov.ma.tce.obralegal.server.tipoexecucao;

public enum TipoExecucao {

	/*
	 * Execução direta: obras e serviços executados diretamente pelo ente
	 * Execução indireta: obras e serviços executados por licitação (empresa)
	 */
	EXECUCAO_DIRETA("EXECUÇÃO DIRETA"), EXECUCAO_INDIRETA("EXECUÇÃO INDIRETA");

	private String descricao;

	private TipoExecucao(String descricao) {
		this.descricao = descricao;
	}

	public String getDescricao() {
		return descricao;
	}

}
